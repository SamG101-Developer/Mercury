from enum import Enum


class ConnectionProtocol(Enum):
    REGISTER = 0b00000001
    REGISTER_ACK_AND_CERT = 0b00000010
    CLIENT_ONLINE = 0b00000011
    CLIENT_ONLINE_ACK = 0b00000100
    GET_NODE_INFO = 0b00000101
    NODE_INFO = 0b00000110
    SOLO_KEX = 0b00001000
    SOLO_KEX_ACK = 0b00001001
    SEND_MESSAGE = 0b00001010
    CREATE_GC = 0b00001011
    CREATE_GC_ACK = 0b00001100
    PREP_FOR_GC = 0b00001110
    GC_INVITE = 0b00001111
    GC_CONFIRM = 0b00010000
    SEND_GROUP_MESSAGE = 0b00010001
    MESSAGE_ACK = 0b00010010
    SOLO_INVITE = 0b00010011
    ERROR = 0b11111111
