from enum import Enum


class ConnectionProtocol(Enum):
    REGISTER = 0b00000001
    REGISTER_ACK_AND_CERT = 0b00000010
    CLIENT_ONLINE = 0b00000011
    CLIENT_ONLINE_ACK = 0b00000100
    GET_NODE_INFO = 0b00000101
    NODE_INFO = 0b00000110
    SEND_MESSAGE = 0b00000111
    CREATE_GC = 0b00001000
    CREATE_GC_ACK = 0b00001001
    GC_INVITE = 0b00001010
    MESSAGE_ACK = 0b00001011
    SOLO_INVITE = 0b00001100
    ERROR = 0b11111111
